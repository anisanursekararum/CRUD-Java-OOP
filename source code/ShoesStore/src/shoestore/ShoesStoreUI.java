    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        // action ketika tombol "Tambah Item" diklik
        
        if(txtQuantity.getText().equals("")||shoesId.getSelectedItem().equals("-")||txtPrice.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Semua field!");
        } else {
            String id = (String) shoesId.getSelectedItem();
            String name = txtShoes_Name.getText();
            String quantity = txtQuantity.getText();
            String price = txtPrice.getText();
            String sub_total;
            int qty , prc;
            
            // validasi input integer untuk field quantity dan price
            try {
                qty = Integer.parseInt(quantity);
                prc = Integer.parseInt(price);
                int subtotal = qty * prc;
                sub_total = String.valueOf(subtotal);
                String data[] = {id,name,quantity,price,sub_total};
                DefaultTableModel tblModel = (DefaultTableModel)txtSalesDetail.getModel();
                tblModel.addRow(data);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Quantity atau Price tidak valid!\n(wrong number format)");
                txtQuantity.setText("");
                txtPrice.setText("");
            }
            
            
        }
        calculateGrandTotal();
    }//GEN-LAST:event_jButton2MouseClicked

