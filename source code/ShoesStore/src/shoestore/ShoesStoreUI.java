    private void getShoesID(){
        // isi list ComboBox Shoes_ID dengan mengambil data dari tabel Shoes, field Shoes_ID
        Connection conn = getConnection();
        try {
            String id, nama;
            java.sql.Statement stmt = conn.createStatement();
            java.sql.ResultSet res = stmt.executeQuery("SELECT * FROM `shoes` WHERE '1'");
            while (res.next()) {
                id = res.getString("Shoes_ID");
                nama = res.getString("Shoes_Name");
                shoes_id[jml_shoes][0] = id;
                shoes_id[jml_shoes][1] = nama;
                jml_shoes++;
            }
            
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        for(int i=0; i<jml_shoes; i++){
            shoesId.addItem(shoes_id[i][0]);
        }
    }
    
    public String TampilData(String vendorCode) {
        // Fungsi bantuan untuk mengambil data nama user berdasarkan ID dari database
        Connection conn = getConnection();
        String nama = null;
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "SELECT * FROM `customer` WHERE `Customer_ID` = '" + vendorCode + "'";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                nama = res.getString("Customer_Name");
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        return nama;
    }
    
    private void calculateGrandTotal() {
        DefaultTableModel tblModel = (DefaultTableModel)txtSalesDetail.getModel();
        int GrandTotal = 0;
        for(int i=0; i < tblModel.getRowCount(); i++){
            GrandTotal += Integer.parseInt((String) tblModel.getValueAt(i, 4));
        }
        String strGrandTotal = String.valueOf(GrandTotal);
        if(GrandTotal == 0){
            txtGrandTotal.setText("Rp. 0");
        } else {
            txtGrandTotal.setText("Rp. "+strGrandTotal);
        }
    }
    
    public String formatDate(String str){
        String date = "", month = "", year = "";
        int iend = str.indexOf("-"); 
        if (iend != -1)
            date = str.substring(0 , iend);
        str = str.substring(iend+1, str.length());
        iend = str.indexOf("-"); 
        if (iend != -1)
            month = str.substring(0 , iend);
        year = str.substring(iend+1, str.length());
        if(date.length()==1)
            date = "0" + date;
        if(month.length()==1)
            month = "0" + month;
        String result = year + "-" + month + "-" + date;
        return result;
    }
    
