    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // Menampilkan data nama user berdasarkan input ID, ketika klik tombol view
        JFrame jFrame = new JFrame();
        String vendorCode = txtCustomer_ID.getText();
        if("".equals(vendorCode)){
            JOptionPane.showMessageDialog(jFrame, "Mohon isi field Customer ID");
        }
        else {
            String outputName = TampilData(vendorCode);
            if(outputName == null){
                txtCustomer_ID.setText("");
                txtViewCustomer_Nama.setText("");
                JOptionPane.showMessageDialog(jFrame, "Customer Belum Terdaftar!");
            } else {
                txtViewCustomer_Nama.setText(outputName);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

