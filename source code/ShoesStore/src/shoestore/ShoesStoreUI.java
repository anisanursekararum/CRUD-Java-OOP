    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        // TODO add your handling code here:
        // action ketika tombol "simpan" diklik
        if(txtSales_ID.getText().equals("")||txtSales_Date.getText().equals("")||txtCustomer_ID.getText().equals("")||txtCustomer_RefNo.getText().equals("")||txtViewCustomer_Nama.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Semua field!");
        } else {
            if(txtSalesDetail.getRowCount()==0) {
                JOptionPane.showMessageDialog(this, "Tabel Kosong!\nHarap Isi data terlebih dahulu!");
            } else {
                String id_sales = txtSales_ID.getText().replace(" ", "");
                String id_cust = txtCustomer_ID.getText().replace(" ", "");
                String ref_cust = txtCustomer_RefNo.getText();
                String date = formatDate(txtSales_Date.getText());
                Date date_formatted;
                // cek format tanggal 
                try {
                    date_formatted = Date.valueOf(date);
                    
                    // insert dan update data ke db
                    try {
                        // insert data ke tabel sales
                        Connection conn = getConnection();
                        String query = "INSERT INTO `sales`(`Sales_ID`, `Sales_Date`, `Customer_ID`, `Customer_RefNo`) VALUES (?,?,?,?)";
                        PreparedStatement preparedStmt = conn.prepareStatement(query);
                        preparedStmt.setInt(1, Integer.parseInt(id_sales));
                        preparedStmt.setDate(2, date_formatted);
                        preparedStmt.setInt(3, Integer.parseInt(id_cust));
                        preparedStmt.setString(4, ref_cust);
                        preparedStmt.execute();
                        
                        // insert data ke tabel sales_detail
                        try {
                            DefaultTableModel tblModel = (DefaultTableModel)txtSalesDetail.getModel();
                            for(int i=0; i < tblModel.getRowCount(); i++){
                                // insert data ke tabel sales_detail
                                String shoes_id = (String) tblModel.getValueAt(i, 0);
                                String qty = (String) tblModel.getValueAt(i, 2);
                                String price = (String) tblModel.getValueAt(i, 3);

                                query = "INSERT INTO `sales_detail`(`Sales_ID`, `Shoes_ID`, `Quantity`, `Price`) VALUES (?,?,?,?)";
                                PreparedStatement preparedStmtInsert = conn.prepareStatement(query);
                                preparedStmtInsert = conn.prepareStatement(query);
                                preparedStmtInsert.setInt(1, Integer.parseInt(id_sales));
                                preparedStmtInsert.setInt(2, Integer.parseInt(shoes_id));
                                preparedStmtInsert.setInt(3, Integer.parseInt(qty));
                                preparedStmtInsert.setInt(4, Integer.parseInt(price));
                                preparedStmtInsert.execute();

                                // update stock
                                int stock = 0;
                                java.sql.Statement stmt = conn.createStatement();
                                query = "SELECT * FROM `shoes` WHERE Shoes_ID = '"+Integer.parseInt(shoes_id)+"';";
                                java.sql.ResultSet res = stmt.executeQuery(query);
                                while (res.next()) {
                                    stock = Integer.parseInt(res.getString("Stock"));
                                }

                                // menggunakan rumus stock = stock - quantity
                                stock = stock - Integer.parseInt(qty);
                                query = "UPDATE `shoes` SET `Stock`=? WHERE Shoes_ID = ?";
                                PreparedStatement preparedStmtUpdate = conn.prepareStatement(query);
                                preparedStmtUpdate = conn.prepareStatement(query);
                                preparedStmtUpdate.setInt(1, stock);
                                preparedStmtUpdate.setInt(2, Integer.parseInt(shoes_id));
                                preparedStmtUpdate.executeUpdate();
                            }
                            // tampilkan pesan berhasil
                            JOptionPane.showMessageDialog(this, "Tambah Data Berhasil!!");
                            // kosongkan form
                            txtSales_ID.setText("");
                            txtSales_Date.setText("");
                            txtCustomer_ID.setText("");
                            txtViewCustomer_Nama.setText("");
                            txtCustomer_RefNo.setText("");
                            txtGrandTotal.setText("Rp. 0");
                            shoesId.setSelectedIndex(0);
                            txtShoes_Name.setText("");
                            txtQuantity.setText("");
                            txtPrice.setText("");
                            for(int i=0; i<=txtSalesDetail.getRowCount(); i++){
                                tblModel.removeRow(0);
                            }
                        } catch (SQLException e) {
                            JOptionPane.showMessageDialog(this, "ERROR! : Insert Data ke tabel 'sales_detail'\n" + e.getMessage());
                        }
                        
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, "ERROR! : Insert Data ke tabel 'sales'\n" + e.getMessage());
                    }
                } catch(IllegalArgumentException e) {
                    JOptionPane.showMessageDialog(this, "ERROR!\nDate Format Invalid");
                }
                
            }
        }
    }//GEN-LAST:event_jButton4MouseClicked

   
